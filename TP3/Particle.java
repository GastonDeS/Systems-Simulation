

public class Particle {

    private final float mass;
    private final float radius;
    private float posX;
    private float posY;
    private float velX;
    private float velY;

    public Particle(float mass, float radius, float posX, float posY, float velX, float velY) {
        this.mass = mass;
        this.radius = radius;
        this.posX = posX;
        this.posY = posY;
        this.velX = velX;
        this.velY = velY;
    }


}
